CREATE TABLE EMPLOYEE_ADDRESS(
	EMPLOYEE_ADDRESS_ID INT IDENTITY(1,1) PRIMARY KEY,
	EMPLOYEE_ID INT,
	PERMANENT_ADDRESS VARCHAR(200) NOT NULL,
	HOUSE_NO VARCHAR(200) NOT NULL,
	STREET_NAME VARCHAR(200) NOT NULL,
	CITY VARCHAR(200) NOT NULL,
	DISTRICT VARCHAR(200) NOT NULL,
	STATE VARCHAR(200) NOT NULL,
	PINCODE VARCHAR(200) NOT NULL,
	COUNTRY VARCHAR(200) NOT NULL,
	TEMPORARY_ADDRESS VARCHAR(200) NOT NULL,
	T_HOUSE_NO VARCHAR(200) NOT NULL,
	T_STREET_NAME VARCHAR(200) NOT NULL,
	T_CITY VARCHAR(200) NOT NULL,
	T_DISTRICT VARCHAR(200) NOT NULL,
	T_STATE VARCHAR(200) NOT NULL,
	T_PINCODE VARCHAR(200) NOT NULL,
	T_COUNTRY VARCHAR(200) NOT NULL,
	FOREIGN KEY(EMPLOYEE_ID) REFERENCES EMPLOYEE_DETAILS(EMPLOYEE_ID),
	CREATED_DATE_TIME DATETIME,
	UPDATED_DATE_TIME DATETIME
);