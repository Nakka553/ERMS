CREATE TABLE EMPLOYEE_EDUCATION(
	EMPLOYEE_EDUCATION_DETAILS_ID INT IDENTITY(1,1) PRIMARY KEY,
	EMPLOYEE_ID INT,
	TENTH VARCHAR(200) NOT NULL,
	BOARD_NAME VARCHAR(200) NOT NULL,
	PASSEDOUT_YEAR VARCHAR(200) NOT NULL,
	PERCENTAGE VARCHAR(200) NOT NULL,
	INTERMEDIATE VARCHAR(200)NOT NULL,
	INTER_BOARD_NAME VARCHAR(200) NOT NULL,
	INTER_PASSEDOUT_YEAR VARCHAR(200) NOT NULL,
	INTER_PERCENTAGE DECIMAL(2,2) NOT NULL,
	GRADUATION VARCHAR(200),
	UG_UNIVERSITY VARCHAR(200) NOT NULL,
	UG_PASSEDOUT_YEAR VARCHAR(200) NOT NULL,
	UG_PERCENTAGE DECIMAL(2,2) NOT NULL,
	POST_GRADUTATION VARCHAR(200),
	PG_UNIVERSITY VARCHAR(200),
	PG_PASSEDOUT_YEAR VARCHAR(200),
	PG_PERCENTAGE DECIMAL(2,2),
	CREATED_DATE_TIME DATETIME,
	UPDATED_DATE_TIME DATETIME,
	FOREIGN KEY(EMPLOYEE_ID) REFERENCES EMPLOYEE_DETAILS(EMPLOYEE_ID)
);